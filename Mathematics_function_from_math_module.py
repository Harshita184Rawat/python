import math
# math is module in python that contains all the mathematical functions 
print(math.log(2)) 
print(math.ceil(3.9))
print(math.floor(2.3))
print(math.sqrt(16))
print(math.pow(2,10))
print(math.fmod(43,5)) # fmod returns the modulus 43%5
print(math.isfinite(3.123456789)) # isfinite return true if the number is finite otherwise false
print(math.isqrt(5)) # isqrt return the integer value of the square root of the number\
print(math.cbrt(81)) # cbrt return the cube root
print(math.exp(3)) # exp returns e^x 
# Refer python3 math module document for more such methods
